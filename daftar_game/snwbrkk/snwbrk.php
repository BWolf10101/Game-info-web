    <li>
        <p>
        Pemain mengambil peran sebagai Sensei, seorang penasihat di Schale, organisasi yang mengelola berbagai akademi di Kivotos. Pemain bertugas untuk membimbing dan membantu para siswa akademi, yang kebanyakan adalah gadis-gadis dengan kemampuan bertarung unik. 
        Para siswa ini terlibat dalam misi yang penuh aksi dan konspirasi, serta menghadapi konflik antar akademi. Pemain juga akan menyelidiki misteri yang ada di Kivotos, membangun hubungan dengan para karakter, dan mengungkap rahasia kota tersebut.

    <br>
    <h3><b>2.Gameplay</b></h3>
    <li>
        <p>Sistem Pertarungan: Blue Archive menggunakan sistem pertarungan real-time, di mana pemain dapat memilih dan mengatur tim karakter untuk menghadapi musuh. 
            Setiap karakter memiliki skill spesial yang bisa diaktifkan di saat tertentu, memungkinkan pemain untuk mengatur strategi dengan baik.</p>
    </li>
    <li>
        <p>Pengumpulan Karakter (Gacha): Pemain bisa mendapatkan karakter melalui sistem gacha, di mana setiap karakter memiliki kelangkaan dan kemampuan unik. 
            Karakter ini bisa diperkuat dengan menaikkan level, meningkatkan peralatan, dan membuka skill khusus.</p>
</li>
    <li>
